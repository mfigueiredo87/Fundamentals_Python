# conversao ou coercao de tipos
print(1+1)
print('a' + 'b')

# os tipos int, float, str e bool sao tipos imutaveis e primitivos
print(type(float('1') + 1)) #mostra o tipo de dados convertido

# converter str em int
print(int('1'), type('1'))

# convertendo para bool (quando vazio da true e se preenchido fica false)
print(bool(' '))

# converter para str
print(str(11) + 'b')
