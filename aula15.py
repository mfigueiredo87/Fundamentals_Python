nome = input("Digite seu nome: ")
print("Seu nome é", nome)
# para exibir o valor da variavel nome
print(f"Seu nome é  {nome=}")

# exemplos com numeros, usando a soma
numero1 = int(input("Digite o primeiro numero: "))
numero2 = float(input("Digite o segundo numero: "))
soma = numero1 + numero2
print("A soma entre", numero1, "e", numero2, "é igual a", soma)
# exibindo os valores e as variaveis com f string
print(f"A soma entre {numero1=} e {numero2=} é igual a {soma=}")

