# concatenacao
concatenacao = "Manuel " + '- ' + "Figueiredo!"
print(concatenacao)
# repetição
repeticao = "Manuel" * 3 
print(repeticao)
# operadores de comparação
igualdade = (5 == 5)
diferenca = (5 != 3)
maior_que = (10 > 5)
menor_que = (3 < 7)
maior_ou_igual = (5 >= 5)
menor_ou_igual = (4 <= 6)
print("Igualdade:", igualdade)
print("Repetição:", repeticao, sep="--> ")
print("Diferença:", diferenca)
print("Maior que:", maior_que)
print("Menor que:", menor_que)
print("Maior ou igual:", maior_ou_igual)
print("Menor ou igual:", menor_ou_igual)
