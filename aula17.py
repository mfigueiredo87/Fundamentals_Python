# Operadores relacionais em Python
a = 2
b = 20
print("a =", a)
print("b =", b) 
print("Verifique se: a == b:", a == b)   # Igualdade
print("Verifique se: a != b:", a != b)   # Diferença
print("Verifique se: a < b:", a < b)     # Menor que
print("Verifique se: a <= b:", a <= b)   # Menor ou igual a
print("Verifique se: a > b:", a > b)     # Maior que
print("Verifique se: a >= b:", a >= b)   # Maior ou igual a
